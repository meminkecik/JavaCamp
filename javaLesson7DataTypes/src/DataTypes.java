public class DataTypes {
    public static void main(String[] args) {
        int sayi = 12; // int veri tipinin tam sayı değeri alması gerekir. 4 byte yer kaplar. -2147483648 - 2147483648 aralığında değerler alır.
        byte sayi2 = 12; // byte veri tipinin tam sayı değeri alması gerekir. 1 byte yer kaplar. -128 - 127 aralığında değerler alır.
        short sayi3 = 12; // short veri tipinin tam sayı değeri alması gerekir. 2 byte yer kaplar -32768 - 32767 aralığında değerler alır.
        long sayi4 = 1200000000; // long veri tipinin tam sayı deperi alması gerekir. 8 byte yer kaplar.
        double sayi5= 12.50; // double ve float veri tipi ondalıklı değerler alabilir. float 4, double 8 byte yer kaplar. Double daha yüksek  ve alçak sayıları alabilir.
        char karakter = 'A'; // char veri tipi tek karakter olacak şekilde veri tutar. 2 bytelik yer kaplar.
        boolean varMi = true; // boolean veri tipi true ya da false döndürür. 1 bytelik yer kaplar.
    }
}
